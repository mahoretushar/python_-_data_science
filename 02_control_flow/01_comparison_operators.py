# We are going to look at comparison operators, we use these operators to compare values.
print(10 > 3)  # True
print(10 >= 3)  # True
print(10 < 20)  # True
print(10 <= 20)  # True
print(10 <= 10)  # True
print(10 == 10)  # True
print(10 == '10')  # False
print(10 != '10')  # True

# We can use comparison operators with strings
print("bag" > "apple")  # True
print("bag" > "BAG")  # True
print(ord("b"))  # to print the ASCII value of 'b'
print("B")  # to print the ASCII value of 'B'
